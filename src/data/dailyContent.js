// Funkce pro získání obsahu konkrétního dne z Firebase
export const getDayContentFromFirebase = async (dayNumber) => {
  try {
    const { doc, getDoc } = await import('firebase/firestore')
    const { db } = await import('../lib/firebase')
    
    const docRef = doc(db, 'dailyContent', `day-${dayNumber}`)
    const docSnap = await getDoc(docRef)
    
    if (docSnap.exists()) {
      return docSnap.data()
    } else {
      // Fallback na lokální obsah
      return getDayContent(dayNumber)
    }
  } catch (error) {
    console.error('Chyba při načítání obsahu z Firebase:', error)
    // Fallback na lokální obsah
    return getDayContent(dayNumber)
  }
}

// Funkce pro získání obsahu konkrétního dne (lokální fallback)
export const getDayContent = (dayNumber) => {
  // Pokud máme konkrétní obsah pro daný den, vrátíme ho
  const specificContent = dailyContent.find(content => content.day === dayNumber)
  if (specificContent) {
    return specificContent
  }

  // Jinak vygenerujeme obecný obsah na základě dne v roce
  const dayOfYear = dayNumber
  const isPhotoDay = dayOfYear % 7 === 1 || dayOfYear % 7 === 0 // Každý týden fotka
  const isDualPhotoDay = dayOfYear % 14 === 0 // Každé dva týdny dvě fotky

  // Generování motivace podle období roku
  let motivation = "Pokračuj ve své cestě za krásnou pletí!"
  let task = "Dnes se zaměř na svou rutinu péče o pleť a pozitivní myšlení."

  // Sezónní přizpůsobení
  const month = Math.ceil(dayOfYear / 30.44) // Přibližný měsíc
  
  if (month <= 3) { // Zima/Jaro
    motivation = "Jarní energie probouzí tvou krásu!"
    task = `🌸 JARNÍ PÉČE:
- Hydratace je klíčová v chladném počasí
- Chraň pleť před větrem
- Přidej vitamín C do své rutiny
- Nezapomeň na SPF i v zimě`
  } else if (month <= 6) { // Jaro/Léto
    motivation = "Letní slunce ti dodává energii!"
    task = `☀️ LETNÍ PÉČE:
- Vysoký SPF je nutnost
- Pij více vody kvůli horku
- Lehčí textury krémů
- Chraň se před přímým sluncem`
  } else if (month <= 9) { // Léto/Podzim
    motivation = "Podzimní klid přináší harmonii!"
    task = `🍂 PODZIMNÍ PÉČE:
- Intenzivnější hydratace
- Regenerace po létě
- Příprava na chladnější počasí
- Vitamíny pro imunitu`
  } else { // Podzim/Zima
    motivation = "Zimní klid je čas na regeneraci!"
    task = `❄️ ZIMNÍ PÉČE:
- Bohatší krémy pro suchou pleť
- Ochrana před mrazem
- Teplé nápoje pro hydrataci
- Péče o rty a ruce`
  }

  return {
    day: dayNumber,
    motivation,
    task,
    isPhotoDay,
    isDualPhotoDay
  }
}

// Funkce pro kontrolu, zda je den foto den
export const isPhotoDay = (dayNumber) => {
  const content = getDayContent(dayNumber)
  return content.isPhotoDay || content.isDualPhotoDay
}

// Funkce pro kontrolu, zda je den dual foto den
export const isDualPhotoDay = (dayNumber) => {
  const content = getDayContent(dayNumber)
  return content.isDualPhotoDay
}

